package main

import (
	"fmt"
)

type Fetch struct {
	body string
	url  string
	urls []string
}

type Fetcher interface {
	// Fetch returns the body of URL and
	// a slice of URLs found on that page.
	Fetch(url string) (body string, urls []string, err error)
}

// Crawl uses fetcher to recursively crawl
// pages starting with url, to a maximum of depth.
func Crawl(url string, depth int, fetcher Fetcher) {
	// TODO: Fetch URLs in parallel.
	// TODO: Don't fetch the same URL twice.
	// This implementation doesn't do either:
	ch1 := make(chan *Fetch)
	visited := map[string]bool {url:true}
	go Collect(url, depth, fetcher, ch1)
	for f := range ch1 {
		if (!visited[f.url]) {
			visited[f.url] = true
			fmt.Printf("%s : %s\n", f.url, f.body)
		}
	}
}

func Collect(url string, depth int, fetcher Fetcher, sink chan *Fetch) {
	_Collect(url, depth, fetcher, sink)
	close(sink)
}

func _Collect(url string, depth int, fetcher Fetcher, sink chan *Fetch) {
	if depth <= 0 {
		return
	}
	body, urls, err := fetcher.Fetch(url)
	if err != nil {
		fmt.Println(err)
	} else {
		for _, u := range urls {
			sink <- &Fetch{body, u, urls}
			_Collect(u, depth-1, fetcher, sink)
		}
	}
}

func main() {
	Crawl("http://golang.org/", 4, fetcher)
}

// fakeFetcher is Fetcher that returns canned results.
type fakeFetcher map[string]*fakeResult

type fakeResult struct {
	body string
	urls []string
}

func (f fakeFetcher) Fetch(url string) (string, []string, error) {
	if res, ok := f[url]; ok {
		return res.body, res.urls, nil
	}
	return "", nil, fmt.Errorf("not found: %s", url)
}

